function generateIssuesHTML(issues: SecurityIssue[]): string {
  // Group by file
  const issuesByFile = new Map<string, SecurityIssue[]>();
  for (const issue of issues) {
    if (!issuesByFile.has(issue.file)) {
      issuesByFile.set(issue.file, []);
    }
    issuesByFile.get(issue.file)!.push(issue);
  }

  let html = '<h2 style="margin-top: 40px;">Issues Found</h2>';

  for (const [file, fileIssues] of issuesByFile) {
    html += `
      <div class="file-group">
        <div class="file-name">${escapeHtml(file)}</div>
        ${fileIssues.map(issue => `
          <div class="issue ${issue.severity}">
            <div class="issue-header">
              <div class="issue-title">${escapeHtml(issue.message)}</div>
              <span class="badge badge-${issue.severity}">${issue.severity}</span>
            </div>
            <div class="location">Location: Line ${issue.line}, Column ${issue.column}${issue.provider ? ` | Provider: ${issue.provider}` : ''}</div>
            <div class="snippet">${escapeHtml(issue.snippet)}</div>
            ${issue.educationalContent ? `
              <div class="education">
                <h4>Why is this a problem?</h4>
                <p>${escapeHtml(issue.educationalContent)}</p>
                ${issue.bestPracticeLink ? `<a href="${issue.bestPracticeLink}" target="_blank">Learn more â†’</a>` : ''}
              </div>
            ` : ''}
          </div>
        `).join('')}
      </div>
    `;
  }

  return html;
}

function escapeHtml(text: string): string {
  const map: Record<string, string> = {
    '&': '&amp;',
    '<': '&lt;',
    '>': '&gt;',
    '"': '&quot;',
    "'": '&#039;'
  };
  return text.replace(/[&<>"']/g, m => map[m]);
}
